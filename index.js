// System codziennych przypomnie≈Ñ weryfikacji (o 00:00)
function startDailyReminders() {
    const scheduleNextReminder = () => {
        const now = new Date();
        const tomorrow = new Date(now);
        tomorrow.setDate(tomorrow.getDate() + 1);
        tomorrow.setHours(0, 0, 0, 0); // Ustaw na 00:00:00
        
        const timeUntilMidnight = tomorrow.getTime() - now.getTime();
        
        setTimeout(async () => {
            // Wys≈Çanie przypomnienia o 00:00
            const guild = client.guilds.cache.first(); // Pierwszy serwer (tw√≥j serwer)
            if (guild) {
                await sendVerificationReminder(guild, false);
            }
            
            // Zaplanuj nastƒôpne przypomnienie
            scheduleNextReminder();
            
        }, timeUntilMidnight);
        
        const hoursUntil = Math.round(timeUntilMidnight / 1000 / 60 / 60);
        console.log(`‚è∞ Nastƒôpne przypomnienie weryfikacji za: ${hoursUntil} godzin (o 00:00)`);
    };
    
    // Zaplanuj pierwsze przypomnienie
    scheduleNextReminder();
}

